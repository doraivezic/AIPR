import math


#===============================================================================
# f1(x,y) = 10*(x^2-y)^2+(1-x)^2
# (Rjesenje: X = (1,1) )
#===============================================================================
def f1(x):
    y = 10. * math.pow( ( math.pow(x[0], 2) - x[1]), 2) + math.pow(1-x[0], 2) 
    return y

#===============================================================================
# f2(x,y) = (x-4)^2 + 4(y-2)^2
# (Rjesenje: X = (4,2) )
#===============================================================================
def f2(x):
    y = math.pow((x[0] - 4), 2) + 4 * math.pow( (x[1]-2), 2)
    return y


#===============================================================================
# f3(x1,x2,x3,x4,x5) = (x1-p1)2 + (x2-p2)2 + ... + (x5-p5)2 
#===============================================================================

def f3(x,p):
    y = math.pow( (x[0]-p[0]), 2) + math.pow( (x[1]-p[1]), 2) + math.pow( (x[2]-p[2]), 2) + math.pow( (x[3]-p[3]), 2) + math.pow( (x[4]-p[4]), 2) 
    return y

#===============================================================================
# f4(x,y) = |(x-y)*(x+y)| + (x^2+y^2)^0.5
# (Rjesenje: X = (0,0) )
#===============================================================================

def f4(x):
    y = abs((x[0]-x[1]) * (x[0]+x[1])) + math.pow( (math.pow(x[0],2) + math.pow(x[1],2)), 0.5)
    return y


